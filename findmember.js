// ===== 순차 조회 함수 =====
async function checkMembersSequential(startIndex = 0, count = members.length) {
    const results = [];

    for (let i = startIndex; i < Math.min(startIndex + count, members.length); i++) {
        const m = members[i];

        try {
            const res = await fetch(
                `https://apis.naver.com/cafe-web/cafe-cafeinfo-api/v3.0/cafes/${CAFE_ID}/members/${m.memberKey}/profiles`,
                {
                    method: "GET",
                    credentials: "include",
                    headers: {
                        "accept": "application/json, text/plain, */*",
                        "x-cafe-product": "pc"
                    }
                }
            );

            const data = await res.json();
            const profile = data?.result ?? {};

            const info = {
                nickname: profile.nickname ?? m.nickname ?? "",
                maskedMemberId: profile.maskedMemberId ?? ""
            };

            console.log(`${info.nickname} | ${info.maskedMemberId}`);
            results.push(info);

        } catch (e) {
            console.log("에러:", m.memberKey);
        }

        // 딜레이
        await new Promise(r => setTimeout(r, DELAY));
    }

    console.table(results);
    return results;
}

